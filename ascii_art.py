def get_asciiart(indexnumber):
    """
    return the asciiart as a string when given an index number corresponding to a page id 
    """
    parts = ascii_array[indexnumber].split('\n')
    width = max([len(i) for i in parts])
    return ascii_array[indexnumber],width
